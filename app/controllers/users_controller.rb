class UsersController < ApplicationController
  skip_before_filter :authorize, :only => [:new, :create, :confirm_account]
  # GET /users
  # GET /users.json
  def index
    @users = User.all

    respond_to do |format|
      format.html # index.html.erb
      format.json { render json: @users }
    end
  end

  # GET /users/1
  # GET /users/1.json
  def show
    @user = User.find(params[:id])

    respond_to do |format|
      format.html # show.html.erb
      format.json { render json: @user }
    end
  end

  # GET /users/new
  # GET /users/new.json
  def new
    @user = User.new

    respond_to do |format|
      format.html # new.html.erb
      format.json { render json: @user }
    end
  end

  # GET /users/1/edit
  def edit
    @user = User.find(params[:id])
  end

  # POST /users
  # POST /users.json
  def create
    @user = User.new(params[:user])

    respond_to do |format|
      if @user.save
        Notifier.new_account(@user, request.remote_ip, request.host_with_port, request.protocol ).deliver
        format.html { redirect_to @user, notice: 'User was successfully created.' }
        format.json { render json: @user, status: :created, location: @user }
      else
        format.html { render action: "new" }
        format.json { render json: @user.errors, status: :unprocessable_entity }
      end
    end
  end

  # PUT /users/1
  # PUT /users/1.json
  def update
    @user = User.find(params[:id])

    respond_to do |format|
      if @user.update_attributes(params[:user])
        format.html { redirect_to @user, notice: 'User was successfully updated.' }
        format.json { head :ok }
      else
        format.html { render action: "edit" }
        format.json { render json: @user.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /users/1
  # DELETE /users/1.json
  def destroy
    @user = User.find(params[:id])
    @user.destroy

    respond_to do |format|
      format.html { redirect_to users_url }
      format.json { head :ok }
    end
  end
  
  def confirm_account
    begin
      @user = User.find(params[:id])
      if @user.confirmation_code == params[:code]
        @user.confirmed = true
        @user.save
        session[:user_id] = @user.id
        Notifier.account_confirmed(@user, request.host_with_port, request.protocol ).deliver
        redirect_to home_url, :alert => 'Account validated successfully!'
      else
        redirect_to home_url, :alert => 'Invalid confirmation code!'
      end
    rescue ActiveRecord::RecordNotFound
      redirect_to home_url, :alert => 'Invalid account'
    end
  end
  
end
